/*
 *   类中,定义方法,实现两个整数的求和,获取求和的结果
 *   
 *   写一份使用说明书,文档注释 /**
 */

/**
 * 
 * 类GetSum实现整数的求和
 * @author JiaLian
 * @version 1.2
 * 
 *          
 * 
 */
public class GetSum {
	/**
	 * 方法GetSum功能计算两个整数的求和 调用次方法传递两个int整数,返回求和值
	 * 
	 * @param a 参数1
	 * @param b 参数2
	 * @return 和
	 */
	public static int getSum(int a, int b) {
		return a + b;
	}
}
