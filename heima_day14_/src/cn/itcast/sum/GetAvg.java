/**
 * 求平均值
 * @version 1.0 GetAvg类实现获取求取平均值
 * @author JiaLian
 * 
 */
public class GetAvg {
	/**
	 * 方法getSum功能计算两个整数的求和
	 * 调用次方法传递两个int整数,返回求和值
	 * @param a
	 * @param b
	 * @return sum
	 */
	public static double getAvg(double a, double b) {
		return (a + b) / 2;
	}
}
