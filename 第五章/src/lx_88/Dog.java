package lx_88;

public class Dog {
	public void bark() {
		System.out.println("狗吠");
	}

	public void bark(int i) {
		System.out.println("咆哮了");
	}

	public void bark(int i, String s) {
		System.out.println("狗吠");
	}

	public void bark(String s, int i) {
		System.out.println("咆哮");
	}
}
